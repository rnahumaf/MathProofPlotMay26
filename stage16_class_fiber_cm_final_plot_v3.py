def write_csv(path: Path, rows: list[dict], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            out = {}
            for key in fieldnames:
                value = row.get(key, "")
                if isinstance(value, (list, tuple)):
                    value = " ".join(str(x) for x in value)
                out[key] = value
            writer.writerow(out)


def exact_key(z) -> str:
    return str(z)


def is_prime(n: int) -> bool:
    if n < 2:
        return False
    if n % 2 == 0:
        return n == 2
    d = 3
    while d * d <= n:
        if n % d == 0:
            return False
        d += 2
    return True


def primes_split_completely_in_cyclotomic(m: int, count: int) -> list[int]:
    """In Q(zeta_m), unramified rational p splits completely iff p ≡ 1 mod m."""
    primes = []
    n = 2
    while len(primes) < count:
        if n % m == 1 and is_prime(n):
            primes.append(n)
        n += 1
    return primes


def near_square_grid_unit_edges(n: int) -> tuple[int, int, int]:
    rows = int(math.floor(math.sqrt(n)))
    cols = int(math.ceil(n / rows))
    occupied = {(idx // cols, idx % cols) for idx in range(n)}
    edges = 0
    for r, c in occupied:
        if (r, c + 1) in occupied:
            edges += 1
        if (r + 1, c) in occupied:
            edges += 1
    return rows, cols, edges


def parse_optional_float_list(items: list[str]) -> list[float | None]:
    out = []
    for item in items:
        if item.lower() in {"none", "null", "nan"}:
            out.append(None)
        else:
            out.append(float(item))
    return out


