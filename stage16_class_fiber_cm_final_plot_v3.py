def make_cm_conjugation(K):
    zeta = K.gen()
    return K.hom([zeta ** (-1)], K)


def conjugate_ideal(K, conj, I):
    return K.ideal([conj(g) for g in I.gens()])


def pair_conjugate_prime_ideals(K, conj, prime_ideals):
    unused = list(prime_ideals)
    pairs = []

    while unused:
        P = unused.pop(0)
        cP = conjugate_ideal(K, conj, P)

        found_idx = None
        for i, Q in enumerate(unused):
            if Q == cP:
                found_idx = i
                break

        if found_idx is None:
            # Fixed by conjugation or Sage comparison failed; not useful here.
            continue

        Q = unused.pop(found_idx)
        pairs.append((P, Q))

    return pairs


def ideal_generated_by(K, g):
    """Try to build the principal fractional ideal generated by g."""
    for method_name in ["ideal", "fractional_ideal"]:
        try:
            return getattr(K, method_name)(g)
        except Exception:
            continue
    return None


def quotient_ideal(I, J):
    """Return I * J^-1, Sage-version-tolerant."""
    try:
        return I * J.inverse()
    except Exception:
        try:
            return I / J
        except Exception:
            return None


def try_principal_generator(K, I):
    """
    Try to obtain a generator g with (g) = I.

    Works for integral ideals in many Sage builds, and attempts fractional ideals
    too. Returns None if Sage cannot prove principality / extract a generator.
    """
    if I is None:
        return None

    try:
        if hasattr(I, "is_principal") and not I.is_principal():
            return None
    except Exception:
        pass

    # Direct principal-generator APIs.
    for method_name in ["principal_generator", "generator"]:
        try:
            g = getattr(I, method_name)()
            J = ideal_generated_by(K, g)
            if J is not None and J == I:
                return g
        except Exception:
            pass

    # Try generators/reduced generators.
    for method_name in ["gens_reduced", "gens"]:
        try:
            gens = list(getattr(I, method_name)())
            for g in gens:
                J = ideal_generated_by(K, g)
                if J is not None and J == I:
                    return g
        except Exception:
            pass

    # Some fractional ideals can be generated by one of their generators only up
    # to denominator normalization; try the principal ideal containment route.
    try:
        gens = list(I.gens())
        if len(gens) == 1:
            return gens[0]
    except Exception:
        pass

    return None


def principal_complex_value(z) -> complex:
    return complex(CC(z))


def principal_angle(z) -> float:
    v = principal_complex_value(z)
    t = math.atan2(v.imag, v.real)
    if t < 0:
        t += 2 * math.pi
    return t


def embedding_values(embeddings, z):
    return [complex(emb(z)) for emb in embeddings]


def build_split_prime_pairs(m: int, split_prime_count: int, verbose: bool):
    K = CyclotomicField(m, "z")
    conj = make_cm_conjugation(K)
    embeddings = K.embeddings(CC)

    primes = primes_split_completely_in_cyclotomic(m, split_prime_count)
    pair_blocks = []
    factor_summary = []

    for p in primes:
        fac = list(K.ideal(p).factor())
        prime_ideals = [P for P, e in fac if e == 1]
        pairs = pair_conjugate_prime_ideals(K, conj, prime_ideals)

        pair_blocks.extend(pairs)

        factor_summary.append(
            {
                "m": m,
                "p": p,
                "prime_ideal_count": len(prime_ideals),
                "conjugate_pair_count": len(pairs),
            }
        )

    if verbose:
        print(f"K = Q(zeta_{m}), degree={K.degree()}")
        print(f"split rational primes: {primes}")
        print(f"conjugate ideal pairs: {len(pair_blocks)}")

    metadata = {
        "m": m,
        "degree": K.degree(),
        "embedding_count": len(embeddings),
        "split_primes": " ".join(str(p) for p in primes),
        "split_prime_count": split_prime_count,
        "conjugate_pair_count": len(pair_blocks),
        "ideal_choices": 1 << len(pair_blocks),
    }

    return K, conj, embeddings, pair_blocks, metadata, factor_summary


def enumerate_product_ideals(K, pair_blocks, max_ideal_choices: int):
    total_pairs = len(pair_blocks)
    total_choices = 1 << total_pairs

    if total_choices > max_ideal_choices:
        raise RuntimeError(
            f"Too many ideal choices: 2^{total_pairs}={total_choices}, "
            f"exceeds --max-ideal-choices={max_ideal_choices}."
        )

    one = K.ideal(1)
    products = []

    for mask in range(total_choices):
        I = one
        for j, (P, Q) in enumerate(pair_blocks):
            I *= Q if ((mask >> j) & 1) else P
        products.append({"mask": mask, "ideal": I})

    return products


