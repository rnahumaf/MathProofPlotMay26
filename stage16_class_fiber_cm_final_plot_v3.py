def cluster_by_principal_ratios(K, products: list[dict], verbose: bool):
    """
    Greedy clustering by ideal class.

    For a product ideal I and a bucket representative I0, test whether I/I0 is
    principal. If yes, I is in the same ideal class and the generator alpha of
    I/I0 is stored.

    This avoids relying on Sage's class-group API, and directly produces the
    principal ratios needed for norm-one translations.
    """
    buckets = []
    ratio_tests = 0
    ratio_successes = 0
    ratio_failures = 0

    for idx, row in enumerate(products):
        I = row["ideal"]
        assigned = False

        for bucket_id, bucket in enumerate(buckets):
            J = quotient_ideal(I, bucket["rep_ideal"])
            ratio_tests += 1
            alpha = try_principal_generator(K, J)

            if alpha is not None:
                ratio_successes += 1
                bucket["members"].append(
                    {
                        "mask": row["mask"],
                        "ideal": I,
                        "alpha": alpha,
                        "bucket_id": bucket_id,
                    }
                )
                assigned = True
                break
            else:
                ratio_failures += 1

        if not assigned:
            bucket_id = len(buckets)
            buckets.append(
                {
                    "bucket_id": bucket_id,
                    "rep_mask": row["mask"],
                    "rep_ideal": I,
                    "members": [
                        {
                            "mask": row["mask"],
                            "ideal": I,
                            "alpha": K(1),
                            "bucket_id": bucket_id,
                        }
                    ],
                }
            )

        if verbose and (idx + 1) % 1000 == 0:
            best = max(len(b["members"]) for b in buckets)
            print(
                f"  clustered {idx+1}/{len(products)} products; "
                f"buckets={len(buckets)} best_bucket={best}"
            )

    bucket_rows = []
    for b in buckets:
        bucket_rows.append(
            {
                "bucket_id": b["bucket_id"],
                "rep_mask": b["rep_mask"],
                "bucket_size": len(b["members"]),
            }
        )

    stats = {
        "class_bucket_count": len(buckets),
        "principal_ratio_tests": ratio_tests,
        "principal_ratio_successes": ratio_successes,
        "principal_ratio_failures": ratio_failures,
        "largest_bucket_size": max((len(b["members"]) for b in buckets), default=0),
    }

    return buckets, bucket_rows, stats


def make_norm_one_translation_rows(K, conj, embeddings, bucket):
    """
    For members in one ideal-class bucket, alpha generates I/I0.
    Build u = alpha / c(alpha), so u*c(u)=1 exactly.
    """
    rows = []
    seen = set()

    for member in bucket["members"]:
        alpha = member["alpha"]
        calpha = conj(alpha)

        if calpha == 0:
            continue

        u = alpha / calpha

        # Exact check.
        try:
            if u * conj(u) != 1:
                continue
        except Exception:
            continue

        # Deduplicate u and -u, because they give the same undirected direction.
        u_key_signed = exact_key(u)
        u_key = min(u_key_signed, exact_key(-u))

        if u_key in seen:
            continue
        seen.add(u_key)

        vals = embedding_values(embeddings, u)
        abs_vals = [abs(v) for v in vals]

        rows.append(
            {
                "u": u,
                "u_key": u_key_signed,
                "mask": member["mask"],
                "angle": principal_angle(u),
                "principal_abs": float(abs_vals[0]),
                "max_abs_all_embeddings": float(max(abs_vals)),
                "embedding_values": vals,
            }
        )

    rows.sort(key=lambda r: r["angle"])
    return rows


