#!/usr/bin/env sage -python
"""
Stage 16: CM split-prime ideal-class fibers + norm-one ratios + centered polydisc.

This is the most proof-aligned finite engine so far.

Motivation
----------
Earlier stages used:
    - explicit Gaussian split-prime fibers in Q(i);
    - principal products of split prime ideals in Q(zeta_m);
    - same-norm fibers found by coefficient search.

The paper's asymptotic mechanism is stronger:
    many products of split prime ideals are forced, by class-group pigeonholing,
    to lie in the same ideal class. Ratios of ideals in the same class are
    principal. Their generators produce algebraic elements whose CM quotients
    have relative norm 1.

Stage 16 implements this finite analogue:

    K = Q(zeta_m), a CM field.
    Pick rational primes p == 1 mod m, so they split completely in K.
    Pair prime ideals above p under CM conjugation.
    Enumerate choices of one ideal from each conjugate pair.
    Cluster these product ideals by ideal class using principal-ratio tests.
    For a large class-fiber:
        I / I0 = (alpha)
        u = alpha / c(alpha)
    Then:
        u*c(u) = 1 exactly.
    Use selected u's as unit translations in every complex embedding.
    Build centered subset sums:
        sum_j (eps_j - 1/2) sigma(u_j)
    Keep points in a Minkowski product-of-discs:
        max_sigma |...| <= R
    Project to sigma_1(K) ~= C and count unit-distance edges.

Default file policy:
    - summary CSVs only
    - no huge points/edges/translations files unless --save-details
    - no PNG unless --plot-best
"""

